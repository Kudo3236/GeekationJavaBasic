package basicSyntax;

public class Variables {

	public static void main(String[] args) {
		int num1 = 16;
		double num2 = 5.3;
		String like = "私の好きな";
		String word1 = "は";
		String word2 = "です。";
		System.out.println(num1);
		System.out.println(num2);
		System.out.println(like + "整数" + word1 + num1 + word2);
		System.out.println(like + "小数" + word1 + num2 + word2);

	}

}
